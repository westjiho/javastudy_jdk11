
public class StringClass {

	// 참조자료형 String 사용하기
	public static void main(String[] args) {
	
		// 1. 문자열 변수 선언/출력
		String name = "Hello World";
		System.out.println(name);

		String a = "10";
		char b = 'a';
		System.out.println(a);
		System.out.println(b);
		
		
		// 리터럴 사용
		System.out.println("AB");
		System.out.println("TAB\t: A	B");
		System.out.println("\\t\t: A\tB");
		System.out.println("\\\"\t: Hello \"W\"orld");
		System.out.println("'\t: Hello 'W'orld");
		System.out.println("\\n\t: 줄바\n꿈");
	
	}
}