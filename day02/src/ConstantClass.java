
public class ConstantClass {
	
	public static void main(String[] args) {
		int age = 45;
		System.out.println(age);
		age = age + 1;
		System.out.println(age);
		
		
		// 변수 pi
		float pi = 3.14f;
		System.out.println(pi);
		pi = pi + 1.00f;
		System.out.println(pi);
		
		
		// 상수 PI
		final float PI = 3.14f;
		System.out.println(PI);
		
	}
}
